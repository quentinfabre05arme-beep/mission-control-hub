class ModelRouter:
    """Routes tasks to optimal models based on use case"""
    
    def __init__(self, config_path="model_router.json"):
        with open(config_path, 'r') as f:
            self.config = json.load(f)
    
    def get_model(self, task_type, prefer_speed=False):
        """Get optimal model for task type"""
        task_config = self.config['models'].get(task_type, self.config['models']['research'])
        
        if prefer_speed:
            return task_config['fallback']
        return task_config['primary']
    
    def get_params(self, task_type):
        """Get generation parameters for task type"""
        task_config = self.config['models'].get(task_type, self.config['models']['research'])
        return {
            'max_tokens': task_config['max_tokens'],
            'temperature': task_config['temperature']
        }

