def main():
    # Fix Windows encoding
    import sys
    if sys.platform == 'win32':
        sys.stdout.reconfigure(encoding='utf-8')
    
    system = ClawSystem()
    
    if len(sys.argv) < 2:
        print(system.help())
        return
    
    command = sys.argv[1]
    args = sys.argv[2:]
    
    try:
        if command == "research":
            if len(args) < 1:
                print("Usage: research <query> [depth]")
                return
            query = args[0]
            depth = args[1] if len(args) > 1 else "medium"
            system.run_research(query, depth)
        
        elif command == "content-x":
            if len(args) < 1:
                print("Usage: content-x <topic> [tweets]")
                return
            topic = args[0]
            num_tweets = int(args[1]) if len(args) > 1 else 5
            system.run_content_x(topic, num_tweets)
        
        elif command == "content-yt":
            if len(args) < 1:
                print("Usage: content-yt <topic> [minutes]")
                return
            topic = args[0]
            duration = int(args[1]) if len(args) > 1 else 10
            system.run_content_youtube(topic, duration)
        
        elif command == "website":
            if len(args) < 2:
                print("Usage: website <name> <description>")
                return
            name = args[0]
            description = args[1]
            features = args[2:] if len(args) > 2 else None
            system.run_website(name, description, features)
        
        elif command == "app":
            if len(args) < 2:
                print("Usage: app <name> <description> [platform]")
                return
            name = args[0]
            description = args[1]
            platform = args[2] if len(args) > 2 else "python"
            system.run_app(name, description, platform)
        
        elif command == "sales-offer":
            if len(args) < 3:
                print("Usage: sales-offer <service_name> <target_audience> <price_range>")
                return
            service_name = args[0]
            target_audience = args[1]
            price_range = args[2]
            system.run_sales_offer(service_name, target_audience, price_range)
        
        elif command == "sales-proposal":
            if len(args) < 2:
                print("Usage: sales-proposal <client_name> <project_description> [deliverables...]")
                return
            client_name = args[0]
            project_description = args[1]
            deliverables = args[2:] if len(args) > 2 else ["Initial setup", "Implementation", "Training"]
            system.run_sales_proposal(client_name, project_description, deliverables)
        
        elif command in ["help", "-h", "--help"]:
            print(system.help())
        
        else:
            print(f"Unknown command: {command}")
            print(system.help())
    
    except Exception as e:
        print(f"Error: {e}")
        import traceback
        traceback.print_exc()

if __name__ == "__main__":
    main()
