class ClawSystem:
    """Unified automation system"""
    
    def __init__(self):
        self.router = ModelRouter()
        self.research = ResearchPipeline(self.router)
        self.content = ContentEngine(self.router)
        self.code = CodeGenerator(self.router)
        self.sales = SalesSystem(self.router)
    
    def run_research(self, query, depth="medium"):
        """Run research pipeline"""
        print(f"🔍 Researching: {query}")
        filepath = self.research.research_topic(query, depth)
        print(f"✅ Report saved to: {filepath}")
        return filepath
    
    def run_content_x(self, topic, num_tweets=5):
        """Generate X thread"""
        print(f"📝 Generating X thread: {topic}")
        filepath = self.content.generate_x_thread(topic, num_tweets)
        print(f"✅ Thread saved to: {filepath}")
        return filepath
    
    def run_content_youtube(self, topic, duration=10):
        """Generate YouTube script"""
        print(f"🎬 Generating YouTube script: {topic}")
        filepath = self.content.generate_youtube_script(topic, duration)
        print(f"✅ Script saved to: {filepath}")
        return filepath
    
    def run_website(self, name, description, features=None):
        """Generate website"""
        print(f"🌐 Generating website: {name}")
        filepath = self.code.generate_website(name, description, features)
        print(f"✅ Website saved to: {filepath}")
        return filepath
    
    def run_app(self, name, description, platform="python"):
        """Generate application"""
        print(f"💻 Generating {platform} app: {name}")
        filepath = self.code.generate_app(name, description, platform)
        print(f"✅ App saved to: {filepath}")
